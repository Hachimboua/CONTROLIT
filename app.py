def draw_point_history(image, point_history):
    """
    Draw point history trajectory on the image.
    
    Args:
        image (numpy.ndarray): Input image
        point_history (collections.deque): Queue of point history
    
    Returns:
        numpy.ndarray: Image with point history drawn
    """
    for index, point in enumerate(point_history):
        if point[0] != 0 and point[1] != 0:
            cv.circle(image, (point[0], point[1]), 1 + int(index / 2),
                      (152, 251, 152), 2)

    return image


def draw_info(image, fps, mode, number):
    """
    Draw system information on the image.
    
    This function draws:
    1. FPS counter
    2. Current operation mode
    3. Selected number (when in logging mode)
    
    Args:
        image (numpy.ndarray): Input image
        fps (int): Current frames per second
        mode (int): Current operation mode
        number (int): Selected number for logging
    
    Returns:
        numpy.ndarray: Image with system information drawn
    """
    cv.putText(image, "FPS:" + str(fps), (10, 30), cv.FONT_HERSHEY_SIMPLEX,
               1.0, (0, 0, 0), 4, cv.LINE_AA)
    cv.putText(image, "FPS:" + str(fps), (10, 30), cv.FONT_HERSHEY_SIMPLEX,
               1.0, (255, 255, 255), 2, cv.LINE_AA)

    mode_string = ['Logging Key Point', 'Logging Point History']
    if 1 <= mode <= 2:
        cv.putText(image, "MODE:" + mode_string[mode - 1], (10, 90),
                   cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1,
                   cv.LINE_AA)
        if 0 <= number <= 9:
            cv.putText(image, "NUM:" + str(number), (10, 110),
                       cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1,
                       cv.LINE_AA)
    return image


